
public class MySecondCommit {
	
	public static void main(String[] args) {

		/*
		 * Only one of this can be used
		 */

		// Inceremennt
		for (int i = 0; i < 10; i++) {
			System.out.println("i  = " + i);

			// Decerment
			for (int j = 10; j > 0; j--) {
				System.out.println("j  = " + j);
			}
		}

	}

}
